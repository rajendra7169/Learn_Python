# To get the length of string
name = "rajendra"
print(len(name))  # This counts the length of object which "name" variable holds

print(name.endswith("dra"))  # This function checks wether object ends with "dra"
print(name.startswith("aj"))  # This function checks wether object starts with "aj"
print(
    name.capitalize()
)  # This function converts first character of string to upper case and rest to lower case
print(name.upper())  # This uppercase the all the charecters of string
print(name.lower())  # This lowercase all the text of string
