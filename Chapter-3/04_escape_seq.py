# Escape sequence in python

a = "Rajnedra pandey \nKathmandu, Nepal"  # "\n" this is used for get text in new line
print(a)

print(
    "Rajendra Pandey from \tKathmandu, Nepal"
)  # "\t" this is use for get a tab space between text

print('Raja is a good "boy"')  # "\" \"" is used for putting some text in double quote
