# type of variablr
a = 1
t = type(a)
print(t)  # <class 'int'>

b = "Rajendra"
t = type(b)
print(t)  # <class 'str'>

c = 1.0
t = type(c)
print(t)  # <class 'float'>

# to convert the type of variable
x = "12.5"
y = float(x)
t = type(y)
print(t)  # this will convert the string to float

p = 21
q = str(p)
t = type(q)
print(t)  # this will convert the integer to string

s = 92.53
r = int(s)
t = type(r)
print(t)  # this will convert the float to integer

