# Q-3 How do you prevent a python print() function to print a new line at the end.

# Ans: We can use (end="")  paramater to prevent print() function to print a new line at the end.
# Example

print("Hi, Raja ", end="")
print("How are you")

# Output: Hi, Raja How are you
# Explanation: In the above example, we have used end="" parameter to prevent print() function to print a new line at the end.
