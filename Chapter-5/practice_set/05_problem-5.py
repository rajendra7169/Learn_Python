# s = {} what is the type of s?
# Answer: Dictionary (class 'dict')
s = {}
print(type(s))  # Output: <class 'dict'>

# a = () what is the type of a?
# Answer: Tuple (class 'tuple')
a = ()
print(type(a))  # Output: <class 'tuple'>

# b = set() what is the type of b?
# Answer: Set (class 'set')
b = set()
print(type(b))  # Output: <class 'set'>

# c = [] what is the type of c?
# Answer: List (class 'list')
c = []
print(type(c))  # Output: <class 'list'>
